# ley de la suma


# crecimiento lineal
def f(n):

	for i in range(n):
		print(i)

	for i in range(n):
		print(i)

# O(n) + O(n) = O(n+n) = O(2n) = O(n) 
# esta funcion tiene un crecimiento lineal
# en O de n


# crecimiento cuadratica
def f(n):

	for i in range(n):
		print(i)

	for i in range(n*n):
		print(i)

# O(n) + O(n*n) = O(n+n2) = O(n2)
# en O de n al cuadrado
